        <h1 style="text-align: center; color:#ffecae;" ><u>Wiki del juego</u></h1>
        	
            <h2>Descripción:</h2>

        <div class="text">Al comenzar el juego, en la parte izquierda de tu pantalla aparecerá la imagen de una CPU la cual tendrás que clicar, 
            por cada clic obtendrás un bit. Con estos bits podrás comprar distintas mejoras para poder avanzar de manera más rápida. 
            Las mejoras disponibles son, Ticks, Cache, FPS y Transistores. También habrán 3 boosts  los cuales son Hercios, 
            Waffles y Cron. Por último habrá una desventaja llamada cuello de botella. Después de obtener un número de bits tendrás la opción de hacer un BSoD, 
            esta opción hará que comiences desde el inicio pero con un mejora permanente de las mejoras que compres, además las mejoras costarán la mitad. 
            Al llegar al séptimo BSoD el juego terminará, tu objetivo es llegar al séptimo BSoD en el menor tiempo posible.</div>

            <h2>Mejoras del juego:</h2>

            <div class="text"> <b>Ticks:</b> Esta es una mejora que afectará al valor de tus clicks. Por cada tres compras aumentará en 1 el valor que se mejora al comprar una mejora.<span class="ast">*</span>
                <br>
                <p>
                    0 ⟶ +0<br>
                    1 ⟶ +1<br>
                    2 ⟶ +1<br>
                    3 ⟶ +1 +1<br>
                    4 ⟶ +1 +1<br>
                    5 ⟶ +1 +1<br>
                    6 ⟶ +1 +1 +1
    
                </p>

            </div>
            

            <div class="text"> <b>Cache:</b> Esta mejora hará que generes 1 click por segundo de manera pasiva, sin necesidad de clickar.<span class="ast">*</span></div>

            <div class="text"> <b>FPS:</b> Esta mejora hará que generes 10 click por segundo de manera pasiva, sin necesidad de clickar.<span class="ast">*</span></div>

            <div class="text"> <b>Transistores:</b> Esta mejora hará que generes 20 clics por segundo de manera pasiva, sin necesidad de clickar.<span class="ast">*</span></div>

            <div class="text" style="color: rgb(255, 0, 0);">*Por cada mejora que compres, su precio aumentará respecto a la última compra.</div>

            

            <h2>Boosts y desventajas:</h2>

            <div class="text"> <b>Hercios:</b> Hercios es un boost con duración limitada, su duración será de 15 segundos. Durante estos 15 segundos tus clicks aumentarán de manera exponencial.</div>

            <div class="text"> <b>Wafer:</b> Wafer es un boost donde consigues una cantidad de bits de manera instantánea</div>

            <div class="text"> <b>Cron:</b> Cron es un boost de duración limitada, su duración será de 2 minutos. Durante este tiempo tus bits por segundo se verán multiplicados por 42.</div>

            <div class="text"> <b>Cuello de botella:</b> Cuello de botella es una desventaja donde se reduce tus bits por segundo entre 42 durante 2 minutos</div>



            <h2>Definiciones de cada objeto:</h2>

            <div class="text"> <b>Ticks:</b> Es una manera de medir el tiempo utilizada en el juego Minecraft, cada Tick equivale a 50 milisegundos. <a href="creditos.html#referencias">[1]</a></div>

            <div class="text"> <b>Cache:</b> El cache es un componente que guarda datos para que al volver a acceder a ellos, lo haga más rápido. <a href="creditos.html#referencias">[2]</a></div>

            <div class="text"> <b>FPS:</b> FPS significa Fotogramas por Segundo, se utiliza para medir cuantas imágenes muestra por segundo un dispositivo. <a href="creditos.html#referencias">[3]</a></div>

            <div class="text"> <b>Transistores:</b> El transistor es el dispositivo que lleva la corriente por el procesador. <a href="creditos.html#referencias">[4]</a></div>

            <div class="text"> <b>Hercios:</b> Los hercios son la unidad de medida con la que se expresa la frecuencia con la que un evento sucede durante un tiempo determinado. <a href="creditos.html#referencias">[5]</a></div>

            <div class="text"> <b>Wafer:</b> Es una placa fina de un material semiconductor (material el cual puede actuar como conductor o aislante de corriente), con la que construyen microcircuitos. <a href="creditos.html#referencias">[6]</a></div>

            <div class="text"> <b>Cron:</b> Cron es un administrador de tareas de Linux que permite ejecutar comandos en un momento determinado, por ejemplo, cada minuto, día, semana o mes. <a href="creditos.html#referencias">[7]</a></div>

            <div class="text"> <b>Cuello de botella:</b> Cuello de botella en un PC significa que existe un componente que frena el rendimiento general del sistema porque sus especificaciones están descompensadas. <a href="creditos.html#referencias">[8]</a></div>
            
